export function influencerMaper(selectedProfile, selectedInfluencer, state) {

    selectedInfluencer.forEach((influencer, index) => {
        if (selectedProfile.linkedin) {
            const inf = { influencer: influencer, detailRow: linkedinRows() }
            state.influencerDetailLinkedin.push(inf);
        }
        if (selectedProfile.instagram) {
            const inf = { influencer: influencer, detailRow: instagramRows() }
            state.influencerDetailInstagram.push(inf);
        }
        if (selectedProfile.blog) {
            const inf = { influencer: influencer, detailRow: blogRows() }
            state.influencerDetailBlog.push(inf);
        }
        if (selectedProfile.twitter) {
            const inf = { influencer: influencer, detailRow: twitterRows() }
            state.influencerDetailTwitter.push(inf);
        }
        if (selectedProfile.facebook) {
            const inf = { influencer: influencer, detailRow: facebookRows() }
            state.influencerDetailFacebook.push(inf);
        }
        if (selectedProfile.tiktok) {
            const inf = { influencer: influencer, detailRow: tiktokRows() }
            state.influencerDetailTiktok.push(inf);
        }
        if (selectedProfile.youtube) {
            const inf = { influencer: influencer, detailRow: youtubeRows() }
            state.influencerDetailYoutube.push(inf);
        }
    })

}



